export function demoImagesForCar(car: Car): CarImage[] {
  if (!car.hero_image_url) return [];
  return [
    {
      id: `${car.id}-img-0`,
      car_id: car.id,
      url: car.hero_image_url,
      alt: car.name,
      sort_order: 0,
      created_at: now,
    },
  ];
}
